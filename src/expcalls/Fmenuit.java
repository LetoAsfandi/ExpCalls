package expcalls;

/**
 * Fmenuit est une classe décrivant un item d'un menu.
 * @version Mai 2016.
 * @author Thierry Baribaud.
 */
public class Fmenuit {

    private int m6num;
    private String M6name;
    private String M6extname;

    /**
     * @return the m6num
     */
    public int getM6num() {
        return m6num;
    }

    /**
     * @param m6num the m6num to set
     */
    public void setM6num(int m6num) {
        this.m6num = m6num;
    }

    /**
     * @return the M6name
     */
    public String getM6name() {
        return M6name;
    }

    /**
     * @param M6name the M6name to set
     */
    public void setM6name(String M6name) {
        this.M6name = M6name;
    }

    /**
     * @return the M6extname
     */
    public String getM6extname() {
        return M6extname;
    }

    /**
     * @param M6extname the M6extname to set
     */
    public void setM6extname(String M6extname) {
        this.M6extname = M6extname;
    }
    
}
